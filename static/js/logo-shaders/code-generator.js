import { downloadPNG, startRecording, isRecording } from './export.js';

let currentTab = 'code';
let currentCode = '';
let activeCanvas = null;

export function initCodePanel(getCanvas) {
  activeCanvas = getCanvas;

  const tabs = document.querySelectorAll('.code-tab');
  tabs.forEach(tab => {
    tab.addEventListener('click', () => {
      tabs.forEach(t => t.classList.remove('active'));
      tab.classList.add('active');
      currentTab = tab.dataset.tab;
      renderTab();
    });
  });

  document.getElementById('copyBtn').addEventListener('click', () => {
    if (currentTab === 'code') {
      navigator.clipboard.writeText(currentCode).then(() => {
        const btn = document.getElementById('copyBtn');
        btn.textContent = 'Copied!';
        btn.classList.add('copied');
        setTimeout(() => { btn.textContent = 'Copy Code'; btn.classList.remove('copied'); }, 1500);
      });
    }
  });
}

export function updateCodeOutput(effect, props, svgSource) {
  currentCode = generateSnippet(effect, props, svgSource);
  if (currentTab === 'code') renderTab();
}

function renderTab() {
  const body = document.getElementById('outputBody');
  const actions = document.getElementById('codeActions');

  if (currentTab === 'code') {
    actions.innerHTML = '<button class="copy-btn" id="copyBtn">Copy Code</button>';
    body.innerHTML = `<pre class="code-output"><code id="codeOutput">${escapeHTML(currentCode)}</code></pre>`;
    document.getElementById('copyBtn').addEventListener('click', () => {
      navigator.clipboard.writeText(currentCode).then(() => {
        const btn = document.getElementById('copyBtn');
        btn.textContent = 'Copied!';
        btn.classList.add('copied');
        setTimeout(() => { btn.textContent = 'Copy Code'; btn.classList.remove('copied'); }, 1500);
      });
    });
  } else if (currentTab === 'image') {
    actions.innerHTML = '';
    body.innerHTML = `
      <div class="export-panel">
        <button class="export-btn" id="downloadPNG">Download PNG</button>
        <select class="export-select" id="pngScale">
          <option value="1">1x</option>
          <option value="2" selected>2x</option>
          <option value="4">4x</option>
        </select>
      </div>`;
    document.getElementById('downloadPNG').addEventListener('click', () => {
      const canvas = activeCanvas();
      if (canvas) downloadPNG(canvas, 'logo-shader.png', parseInt(document.getElementById('pngScale').value));
    });
  } else if (currentTab === 'video') {
    actions.innerHTML = '';
    body.innerHTML = `
      <div class="export-panel">
        <button class="export-btn" id="recordBtn">${isRecording() ? 'Recording...' : 'Record'}</button>
        <select class="export-select" id="videoDuration">
          <option value="2">2s</option>
          <option value="4" selected>4s</option>
          <option value="8">8s</option>
        </select>
        <span style="font-size:11px;color:var(--editor-text-dim);">Move cursor during recording for dynamic light</span>
      </div>`;
    document.getElementById('recordBtn').addEventListener('click', () => {
      const canvas = activeCanvas();
      if (!canvas || isRecording()) return;
      const btn = document.getElementById('recordBtn');
      btn.textContent = 'Recording...';
      btn.disabled = true;
      startRecording(canvas, parseInt(document.getElementById('videoDuration').value), () => {
        btn.textContent = 'Record';
        btn.disabled = false;
      });
    });
  }
}

function generateSnippet(effect, props, svgSource) {
  if (!effect) return '';
  const svgDataURL = svgSource ? 'data:image/svg+xml;base64,' + btoa(unescape(encodeURIComponent(svgSource))) : '';
  return `<!--
  ${effect.name} — Logo Shader Effect
  Generated by Logo Shaders (ramiroruiz.com/tools/logo-shaders/)
-->

<canvas id="logo-shader" width="1024" height="1024" style="width:512px;height:512px;"></canvas>

<script>
(function() {
  const canvas = document.getElementById('logo-shader');
  const gl = canvas.getContext('webgl2', { preserveDrawingBuffer: true });
  if (!gl) return;

  const img = new Image();
  img.src = '${svgDataURL}';
  img.onload = () => {
    // Render SVG to texture
    const texCanvas = document.createElement('canvas');
    texCanvas.width = texCanvas.height = 512;
    const ctx = texCanvas.getContext('2d');
    const s = Math.min(512 / img.width, 512 / img.height) * 0.8;
    ctx.drawImage(img, (512 - img.width*s)/2, (512 - img.height*s)/2, img.width*s, img.height*s);

    // WebGL setup + ${effect.name} shader
    // See the full fragment shader in the Code output for the complete effect
  };
})();
<\/script>`;
}

function escapeHTML(str) {
  return str.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}
